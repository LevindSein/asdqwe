    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $permission = Permission::findOrFail($id);

        $permission->delete();

        session()->flash('alert', [
            'type' => 'success',
            'msg' => 'Permission berhasil ditambahkan'
        ]);

        return response()->json(true);
    }
